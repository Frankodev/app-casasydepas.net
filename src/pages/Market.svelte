<p>En esta página se mostrará la publicidad, <i>en un futuro habra una sección con un formulario para comerciantes que quieran publicar sus productos y/o servicios los puedan dar de alta</i></p>
<strong>
  <p>El formulario tendra los siguientes campos</p>
</strong>
<ul>
  <li>nombre y apellido del comerciante o del negocio</li>
  <li>que producto o servivio ofrece - una lista (herreria, muebles, quimicos albercas, lavanderia, etc.)</li>
  <li>foto del producto o servicio que ofrece</li>
  <li>descripcion del producto o servicio que ofrece, en donde lo ofrece, y como lo ofrecer</li>
  <li>precio o rengo de los precios</li>
  <li>telefono de contacto</li>
  <li>dirección de contacto</li>
</ul>